CREATE PROC  [usp_rightsSelect]
    @rusers_id INT=null,
    @users_id INT=null
AS
	SET NOCOUNT ON 
	SET XACT_ABORT ON  

	BEGIN TRAN

	select M.module_name ,F.farm_name ,isnull(E.farm_add ,F.farm_add ) farm_add,isnull(E.farm_edit ,F.farm_edit ) farm_edit,
    isnull(E.farm_delete ,F.farm_delete ) farm_delete,isnull(E.farm_view ,F.farm_view ) farm_view,isnull(E.farm_print ,F.farm_print ) farm_print,
    R.farm_add  RAdd,R.farm_edit  REdit,R.farm_delete  RDelete,R.farm_view  RView,R.farm_print  RPrint,F.module_id ,F.farm_id
    from farm F
    join module M on M.module_id =F.module_id 
    left join rights R on R.farm_id=F.farm_id and R.users_id=@rusers_id
    left join rights E on E.farm_id=F.farm_id and E.users_id=@users_id
    where @users_id<>0 and (@users_id=@users_id or @users_id is null)
    order by M.module_name ,F.farm_name 
    
	COMMIT
GO

